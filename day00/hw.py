import turtle

# Set up the screen
turtle.setup(500, 500)

# Draw the house
turtle.forward(100)
turtle.left(90)
turtle.forward(150)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(150)

# Draw the roof
turtle.right(135)
turtle.forward(141)
turtle.left(135)
turtle.forward(100)

turtle.done()









